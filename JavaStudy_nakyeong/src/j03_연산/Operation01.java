package j03_연산;

public class Operation01 {

	public static void main(String[] args) {
		int num = 1;
		
		num = num + 1; 
		
		System.out.println(++num);
		System.out.println(--num);
		System.out.println(num++);
		System.out.println(num--);
		System.out.println(num);
		System.out.println();

	
	}

}
